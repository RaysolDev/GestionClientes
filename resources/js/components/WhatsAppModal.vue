<script setup>
const props = defineProps({
  show: {
    type: Boolean,
    default: false
  },
  whatsappLink: {
    type: String,
    default: ''
  }
});

const emit = defineEmits(['close']);

const close = () => {
  emit('close');
};

const copyLink = () => {
  navigator.clipboard.writeText(props.whatsappLink).then(() => {
    alert('Enlace copiado al portapapeles');
  }).catch(() => {
    // Fallback for older browsers
    const textarea = document.createElement('textarea');
    textarea.value = props.whatsappLink;
    document.body.appendChild(textarea);
    textarea.select();
    document.execCommand('copy');
    document.body.removeChild(textarea);
    alert('Enlace copiado al portapapeles');
  });
};

const openWhatsApp = () => {
  window.location.href = props.whatsappLink;
};
</script>

